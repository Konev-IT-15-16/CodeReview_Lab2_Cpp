int main()
{
    setlocale(LC_ALL, "Russian");

    FStack MyStack;
    int Choice = 0;

    while (true)
    {
        std::cout << "\n--- Меню работы со стеком ---" << std::endl;
        std::cout << "1. Добавить элемент (Add/Push)" << std::endl;
        std::cout << "2. Извлечь верхний элемент (Pop)" << std::endl;
        std::cout << "3. Вывести стек" << std::endl;
        std::cout << "4. Выход" << std::endl;
        std::cout << "Выберите действие: ";

        if (!SafeReadIntFromConsole(Choice))
        {
            continue;
        }

        if (Choice == 4)
        {
            break;
        }

        switch (Choice)
        {
        case 1:
        {
            int Value;
            std::cout << "Введите значение для добавления: ";
            if (SafeReadIntFromConsole(Value))
            {
                MyStack.Push(Value);
            }
            break;
        }
        case 2:
            MyStack.PopAndPrint();
            break;
        case 3:
            MyStack.Display();
            break;
        default:
            std::cout << "Неверный пункт меню." << std::endl;
        }
    }

    return 0;
}